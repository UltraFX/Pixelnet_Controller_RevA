/**
* @brief This function handles USART1 global interrupt.
*/
void USART1_IRQHandler(void)
{
  /* USER CODE BEGIN USART1_IRQn 0 */
	uint32_t dwStatus = READ_REG(USART1->SR);

#ifdef DMX_MODE
//	if(dwStatus & USART_SR_FE)
	if(dwStatus & (uint32_t)(USART_SR_PE | USART_SR_FE | USART_SR_ORE | USART_SR_NE))
	{
		Pix_UART_Break();
	}
#endif

	if(dwStatus & USART_SR_RXNE)
	{
		Pix_UART_CB();
	}
#ifdef USE_HAL_UART
  /* USER CODE END USART1_IRQn 0 */
  HAL_UART_IRQHandler(&huart1);
  /* USER CODE BEGIN USART1_IRQn 1 */
#endif
  /* USER CODE END USART1_IRQn 1 */
}

